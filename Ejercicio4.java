public class Ejercicio4 {

    public static void main(String[] args) {
        int x = 1, y = 2;
        double n = 1.5, m = 2.5;
        /* Apartado A */
        System.out.println("A: " + x + " " + y + " " + n + " " + m);
        /* Apartado B */
        System.out.println("B: " + (x + y));
        /* Apartado C */
        System.out.println("C: " + (x-y));
        /* Apartado D */
        System.out.println("D: " + (x * y));
        /* Apartado E */
        System.out.println("E: " + (x / y));
        /* Apartado F */
        System.out.println("F: " + (x % y));
        /* Apartado G */
        System.out.println("G: " + (n + m));
        /* Apartado H */
        System.out.println("H: " + (n - m));
        /* Apartado I */
        System.out.println("I: " + (n * m));
        /* Apartado J */
        System.out.println("J: " + (n / m));
        /* Apartado K */
        System.out.println("K: " + (n % m));
        /* Apartado L */
        System.out.println("L: " + (x + n));
        /* Apartado M */
        System.out.println("M: " + (y / m));
        /* Apartado N */
        System.out.println("N: " + (y % m));
        /* Apartado O */
        System.out.println("O: " + (x * 2) + " " + (y * 2) + " " + (n * 2) + " " + (m * 2));
        /* Apartado P */
        System.out.println("P: " +( x + y + n + m));
        /* Apartado Q */
        System.out.println("Q: " + (x * y * n * m));

    }
}
